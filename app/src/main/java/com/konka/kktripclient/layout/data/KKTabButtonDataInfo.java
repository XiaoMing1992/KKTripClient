public class KKTabButtonDataInfo extends KKBaseDataInfo {
    /**
     * 菜单栏字体大小
     */
    public int fontSize;
    /**
     * 菜单栏X坐标
     */
    public int menuCoordinateX;
    /**
     * 菜单栏Y坐标
     */
    public int menuCoordinateY;
    /**
     * 菜单栏方向 0:横向 1:竖向
     */
    public int menuDirection;
    /**
     * 菜单栏间距
     */
    public int menuGap;
    /**
     * 菜单栏主键
     */
    public int menuId;
}
